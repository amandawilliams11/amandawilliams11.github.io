
// the function is triggeres when the button with the onclick action is selected. sets the with from 0 (which means its hidden) to its full width of visibility
function openNav() {
  document.getElementById("mySidenav").style.width = "250px";
}


// this function does the opposite when the button is triggered
function closeNav() {
  document.getElementById("mySidenav").style.width = "0";
}
